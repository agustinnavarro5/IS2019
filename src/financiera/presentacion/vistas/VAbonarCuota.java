    @Override
    public void cargarTablaCuotas(ArrayList<Credito> creditos, boolean seSelecciona) {
        boolean[] editable = {false,false,false,false,false,false,false,false,seSelecciona};
        this.tbCuotas.setDefaultRenderer(Object.class, new Render());
        DefaultTableModel dt = new DefaultTableModel(new String[]{"NRO CREDITO", "ESTADO","NRO CUOTA","FECHA VENCIMIENTO", "MONTO INICIAL","INTERES","MONTO NETO", "SALDO PENDIENTE","SELECCIONAR"}, 0) {
 
            Class[] types = new Class[]{
                java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Boolean.class
            };
 
            @Override
            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }
            
            @Override
            public boolean isCellEditable(int row, int column){
                return editable[column];
            }
        };
        if(creditos.size() > 0){
            creditos.forEach((c) -> {
                for(int i=0; i< c.getCuotas().size(); i++){
                    if(c.getCuotas().get(i).getMontoAdeudado()!=0){
                        Object fila[] = new Object[9];
                        fila[0] = c.getNroCredito();
                        fila[1] = c.getEstado().name();
                        fila[2] = c.getCuotas().get(i).getNroCuota();
                        fila[3] = new SimpleDateFormat("dd/MM/yyyy").format(c.getCuotas().get(i).getFechaVencimiento());
                        fila[4] = Math.round(c.getCuotas().get(i).getMontoTotal()*100d)/100d;
                        fila[5] = Math.round(c.getCuotas().get(i).getInteres()*100d)/100d;
                        fila[6] = Math.round(c.getCuotas().get(i).getMontoNeto()*100d)/100d;
                        fila[7] = Math.round(c.getCuotas().get(i).getMontoAdeudado()*100d)/100d;
                        fila[8] = false;
                        dt.addRow(fila);
                    }
                }
            });
        }
        RowSorter<TableModel> sorter = new TableRowSorter<TableModel>(dt);
         this.tbCuotas.setRowSorter(sorter);
        
        this.tbCuotas.setModel(dt);
        this.tbCuotas.getRowSorter().toggleSortOrder(3);
    }

