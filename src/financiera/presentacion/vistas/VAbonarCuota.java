    private void tbCuotasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCuotasMousePressed
        if(evt.getClickCount()==1){
            try{
               if(this.tbCuotas.isEnabled()){
                   int linea  = this.tbCuotas.getSelectedRow();
                    boolean isSelected = this.tbCuotas.isColumnSelected(8);
                    if(isSelected){
                        double monto = (double) this.tbCuotas.getValueAt(linea, 6);
                        boolean tilde = (boolean) this.tbCuotas.getValueAt(linea, 8);
                        if(!tilde){
                            actualizarMontoTotal(monto);
                        }else{
                             actualizarMontoTotal(-monto);
                        }
                    }
               }
                
                
            }catch(Exception ex){
                
            }
        }
    }//GEN-LAST:event_tbCuotasMousePressed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if(!this.lbTotal.getText().startsWith("0")){
            if(this.cbxModoPago.getSelectedItem().equals("Por Monto")){
                    double monto = Double.parseDouble(this.lbTotal.getText());
                    this.presentador.abonarCuota(monto);
          
            }else{
                        for(int i=0;i<this.tbCuotas.getRowCount();i++)
                        {
                            if((boolean)this.tbCuotas.getValueAt(i, 8))
                            {
                                int nroCredito = (int) this.tbCuotas.getValueAt(i, 0);
                                int nroCuota = (int) this.tbCuotas.getValueAt(i, 2);
                                this.presentador.abonarCuota(nroCredito,nroCuota);
                            }
                        }
                        
            }  
            try {
                        this.presentador.finalizarPago();
            } catch (IServicioPublicoCreditoInformarCreditoFinalizadoErrorServicioFaultFaultMessage ex) {
                        Logger.getLogger(VAbonarCuota.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }else{
                    JOptionPane.showMessageDialog(this, "Asegúrese de haber ingresado un monto o de haber seleccionado las cuotas a pagar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

