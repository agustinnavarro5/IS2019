    @Override
    public void cargarDatos(Cliente c) {
        this.txtDni.setText(c.getDni());
        this.txtDni.setEnabled(false);
        this.btnValidar.setEnabled(false);
        this.txtNombre.setText(c.getNombreYApellido());
        this.txtDomicilio.setText(c.getDomicilio());
        this.txtSueldo.setText(String.valueOf(c.getSueldo()));
        this.txtTelefono.setText(c.getTelefono());
    }

    @Override
    public void bloquearIngresoDeDatosAntesDeValidar() {
        this.txtNombre.setEnabled(false);
        this.txtDomicilio.setEnabled(false);
        this.txtSueldo.setEnabled(false);
        this.txtTelefono.setEnabled(false);
        this.btnConfirmar.setEnabled(false);
    }

    private void permitirEntradaDeDatos() {
         this.txtNombre.setEnabled(true);
        this.txtDomicilio.setEnabled(true);
        this.txtSueldo.setEnabled(true);
        this.txtTelefono.setEnabled(true);
        this.btnConfirmar.setEnabled(true);
    }
