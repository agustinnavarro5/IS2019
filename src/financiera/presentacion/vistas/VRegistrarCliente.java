    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        String dni = this.txtDni.getText();
        if(esNumerico(dni)){
           boolean existe = this.presentador.existeCliente(dni);
           if(existe){
               JOptionPane.showMessageDialog(this, "Ya se encuentra un cliente registrado con el dni ingresado.", "Error", JOptionPane.ERROR_MESSAGE);
           }else{
               this.permitirEntradaDeDatos();
           }
        }else{
             JOptionPane.showMessageDialog(this, "Ingrese un dni correcto", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnValidarActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String domicilio = this.txtDomicilio.getText();
        String telefono = this.txtTelefono.getText();
        String sueldo = this.txtSueldo.getText();
        String nombre = this.txtNombre.getText();
        if(esNumerico(sueldo) && esNumerico(telefono)){
            if(!domicilio.isEmpty() && !nombre.isEmpty()){
                if(this.getTitle().startsWith("Registrar")){
                        this.presentador.registrarCliente(true,domicilio,telefono,sueldo,nombre,this.txtDni.getText());
                }else{
                     this.presentador.registrarCliente(false,domicilio,telefono,sueldo,nombre,this.txtDni.getText());
                }
                this.dispose();
                new VGestionCliente(null,true);
            }else{
                JOptionPane.showMessageDialog(this, "Verifique que todos los campos est√©n completos.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Verifique que los campos num√©ricos sean correctos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

   
    
